    def _calculate_overall_health(self) -> HealthStatus:
        """Calculate overall system health status"""
        if not self._component_health:
            return HealthStatus.UNKNOWN
        
        # Count components by status
        status_counts = defaultdict(int)
        for component in self._component_health.values():
            status_counts[component.status] += 1
        
        total_components = len(self._component_health)
        
        # Determine overall health based on component statuses
        if status_counts[HealthStatus.CRITICAL] > 0:
            return HealthStatus.CRITICAL
        elif status_counts[HealthStatus.DEGRADED] > total_components * 0.3:  # More than 30% degraded
            return HealthStatus.DEGRADED
        elif status_counts[HealthStatus.WARNING] > total_components * 0.5:  # More than 50% warning
            return HealthStatus.WARNING
        elif status_counts[HealthStatus.HEALTHY] > total_components * 0.8:  # More than 80% healthy
            return HealthStatus.HEALTHY
        else:
            return HealthStatus.WARNING
    
    def _generate_health_recommendations(self, overall_health: HealthStatus,
                                       active_alerts: List[HealthAlert],
                                       trends: List[HealthTrend]) -> List[str]:
        """Generate health recommendations based on current status"""
        recommendations = []
        
        if overall_health == HealthStatus.CRITICAL:
            recommendations.append("IMMEDIATE ACTION REQUIRED: System is in critical state")
            recommendations.append("Review and resolve all critical alerts immediately")
        
        elif overall_health == HealthStatus.DEGRADED:
            recommendations.append("System performance is degraded - investigate root causes")
            recommendations.append("Consider scaling resources or optimizing performance")
        
        # Alert-based recommendations
        critical_alerts = [a for a in active_alerts if a.severity == AlertSeverity.CRITICAL]
        if critical_alerts:
            recommendations.append(f"Address {len(critical_alerts)} critical alerts immediately")
        
        # Trend-based recommendations
        degrading_trends = [t for t in trends if t.trend_direction == "degrading" and t.trend_strength > 0.5]
        if degrading_trends:
            recommendations.append(f"Monitor {len(degrading_trends)} components showing degrading trends")
        
        # Performance-based recommendations
        if any("queue_depth" in str(a.component) for a in active_alerts):
            recommendations.append("Consider increasing queue processing capacity")
        
        if any("cpu_usage" in str(a.component) for a in active_alerts):
            recommendations.append("Consider CPU optimization or horizontal scaling")
        
        if any("memory_usage" in str(a.component) for a in active_alerts):
            recommendations.append("Consider memory optimization or increasing available memory")
        
        return recommendations
    
