    def acknowledge_alert(self, alert_id: str, acknowledged_by: str) -> bool:
        """Acknowledge an active alert"""
        with self._lock:
            if alert_id in self._active_alerts:
                alert = self._active_alerts[alert_id]
                alert.acknowledged = True
                alert.metadata["acknowledged_by"] = acknowledged_by
                alert.metadata["acknowledged_at"] = datetime.now().isoformat()
                
                logger.info("Alert acknowledged: %s by %s", alert_id, acknowledged_by)
                return True
            return False
    
    def resolve_alert(self, alert_id: str, resolved_by: str, resolution_notes: str = "") -> bool:
        """Resolve an active alert"""
        with self._lock:
            if alert_id in self._active_alerts:
                alert = self._active_alerts[alert_id]
                alert.resolved = True
                alert.resolution_time = datetime.now()
                alert.metadata["resolved_by"] = resolved_by
                alert.metadata["resolution_notes"] = resolution_notes
                
                # Remove from active alerts
                del self._active_alerts[alert_id]
                
                logger.info("Alert resolved: %s by %s", alert_id, resolved_by)
                return True
            return False
    
    def get_active_alerts(self, severity_filter: Optional[AlertSeverity] = None) -> List[HealthAlert]:
        """Get list of active alerts"""
        with self._lock:
            alerts = list(self._active_alerts.values())
            
            if severity_filter:
                alerts = [a for a in alerts if a.severity == severity_filter]
            
            return sorted(alerts, key=lambda x: x.timestamp, reverse=True)
    
    def get_component_health_status(self, component_type: Optional[ComponentType] = None) -> Dict[str, ComponentHealth]:
        """Get health status of components"""
        with self._lock:
            if component_type:
                return {k: v for k, v in self._component_health.items() 
                       if v.component_type == component_type}
            return dict(self._component_health)
    
    def get_health_trends(self, component: Optional[str] = None, hours: int = 24) -> List[HealthTrend]:
        """Get health trends for components"""
        with self._lock:
            trends = list(self._health_trends.values())
            
            if component:
                trends = [t for t in trends if component in t.component]
            
            # Filter by time window
            trends = [t for t in trends if t.time_window_hours <= hours]
            
            return sorted(trends, key=lambda x: x.trend_strength, reverse=True)
    
